DATA SEGMENT    ;小写字母转换为大写
	STRMESSAGE DB "PLEASE INPUT A ZIFU:",0DH,0AH,'$'
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
	MOV DS,AX

	;输出字符串
	MOV DX,OFFSET STRMESSAGE
	MOV AH,09H
	INT 21H 

	;输入字符到AL
	MOV AH,01H
	INT 21H

	;输出字符
	MOV DL,AL
	SUB DL,20H
	MOV AH,02H
	INT 21H 

EXIT:MOV AH,4CH
	INT 21H 

CODE ENDS
	END START 
