;编写程序把从键盘输入的以回车为结束符的字符串中的空格去掉并按相反的顺序输出

DATA SEGMENT
	STR1 DB 'PLEASE INPUT THE STRING',0AH,0DH,'$'
	STR2 DB 'THE RESULT IS:',0AH,0DH,'$'
	LENGTH DW 0H
DATA ENDS
STACK1 SEGMENT PARA STACK
	DW 30H DUP(0)
STACK1 ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA,SS:STACK1
START:
	MOV AX,DATA
	MOV DS,AX

	LEA DX,STR1       ;输出字符串
	MOV AH,09H
	INT 21H

	LOOP1:MOV AH,01H  ;输入字符
	INT 21H
	CMP AL,20H        ;是否是空格
	JE LOOP1
	INC LENGTH
	MOV AH,0
	PUSH AX
	CMP AL,0DH        ;是否是回车
	JNE LOOP1
	
	;MOV DL,0AH        ;输出换行和回车
	;MOV AH,2
	;INT 21H
	;;MOV DL,0DH
	;MOV AH,2
	;INT 21H

	POP DX
	DEC LENGTH
	MOV CX,LENGTH


	LEA DX,STR2       ;输出字符串
	MOV AH,09H
	INT 21H

	LOOP2:POP DX
	MOV AH,2
	INT 21H
	LOOP LOOP2
	MOV AH,4CH
	INT 21H
CODE ENDS
END START