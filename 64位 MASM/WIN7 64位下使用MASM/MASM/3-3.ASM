CODE SEGMENT
	ASSUME CS:CODE,DS:DATA,SS:STACK
START:
	MOV AX,DATA
	MOV DS,AX

	MOV DX,OFFSET STRING1
	MOV AH,09H
	INT 21H

	
	MOV DX,OFFSET STR
	MOV AH,0AH
	INT 21H
	MOV DL,0AH
	MOV AH,O2H
	INT 21H
	MOV DL,0DH
	INT 21H

	MOV DI,OFFSET STR
