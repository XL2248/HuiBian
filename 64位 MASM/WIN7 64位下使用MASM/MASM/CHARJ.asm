DATA SEGMENT
	STR DB "INPUT A LETTER",0AH,0DH,"$"
	STR1 DB "THIS IS A SMALL LETTER",0AH,0DH,"$"
	STR2 DB "THIS IS A CAPITAL LETTER$",0AH,0DH
	STR3 DB 0AH,0DH,"$"
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
	A:
	MOV AX,DATA
	MOV DS,AX
	
	LEA DX,STR
	MOV AH,09H
	INT 21H
	
	MOV AH,01H
	INT 21H
	LEA DX,STR3
	MOV AH,09H
	INT 21H
	
	CMP AL,41H
	JB EXIT
	CMP AL,5AH
	JNAE L1
	
	CMP AL,61H
	JB EXIT
	CMP AL,7AH
	JNAE L2
	JMP EXIT
	
	MOV AH,4CH
	INT 21H
	L1:
	LEA DX,STR1
	MOV AH,09H
	INT 21H
	JMP EXIT
	
	L2:
	LEA DX,STR2
	MOV AH,09H
	INT 21H
	JMP EXIT
	
	EXIT:
	MOV AH,4CH
	INT 21H
	CODE ENDS
END A


	
	