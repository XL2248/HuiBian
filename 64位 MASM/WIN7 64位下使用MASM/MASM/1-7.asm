DATA SEGMENT 
	LIST DW LEN
	     DW 1,3,4,8,13,30
	LEN= ($-LIST)/2-1
	DW 10 DUP(?)
	X DW 5	
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	MOV BX,LIST
	SHL BX,1
	MOV AX,0
NEXT:
	CMP BX,0
	JZ INSERT
	CMP AX,LIST[BX]
	JA INSERT
	MOV DX,LIST[BX]
	MOV LIST[BX+2],DX
	SUB BX,2
	JMP NEXT
INSERT:
	MOV LIST[BX+2],AX
	INC LIST
	MOV AH,4CH
	INT 21H
CODE ENDS
	END START
	